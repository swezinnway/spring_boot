package com.bestbright.onlie_course.model;

import java.io.Serializable;
import java.util.Set;

import javax.persistence.CascadeType;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.OneToOne;

@Entity
public class Student implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	@Id
	@GeneratedValue(strategy=GenerationType.AUTO)
	private Long student_id;
	private String student_name;
	private String DateOfBirth;
	private String email;
	private String NRC;
	private String phone;
	private String Address;
	private String gender;
	
	@OneToOne
	private Student_enrolment student_enrolment;
	
	
	@OneToMany(mappedBy = "student",targetEntity=StudentResult.class,cascade = CascadeType.ALL)

	private Set<StudentResult> studentresultList;
	 
	public Student() {
		
	}
	
	public Long getStudent_id() {
		return student_id;
	}
	public void setStudent_id(Long student_id) {
		this.student_id = student_id;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getDateOfBirth() {
		return DateOfBirth;
	}
	public void setDateOfBirth(String dateOfBirth) {
		DateOfBirth = dateOfBirth;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getNRC() {
		return NRC;
	}
	public void setNRC(String nRC) {
		NRC = nRC;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAddress() {
		return Address;
	}
	public void setAddress(String address) {
		Address = address;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}

	public Student_enrolment getStudent_enrolment() {
		return student_enrolment;
	}

	public void setStudent_enrolment(Student_enrolment student_enrolment) {
		this.student_enrolment = student_enrolment;
	}
	
	
}
